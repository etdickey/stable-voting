# def draw_graph(adj_list, edge_weights, winning_edge=None, file_name=None):
def draw_graph(
    G: nx.DiGraph,
    winning_edge: Optional[Tuple[int, int, int]] = None,
    file_name: Optional[str] = None,
) -> None:
    """
    Draw the tournament with a circular layout and preserve aspect ratio.
    If winning_edge is given, highlight it:
      - If margin >= 0, color (A->B) in red
      - If margin < 0, flip to (B->A) in green
    """
    plt.figure(figsize=(6, 6))
    pos = nx.circular_layout(G)
    plt.axis('off')
    ax = plt.gca()
    ax.set_aspect('equal', adjustable='datalim')

    highlight_edge = None
    highlight_color = None
    if winning_edge is not None:
        A, B, m_val = winning_edge
        if m_val >= 0:
            highlight_edge = (A, B)
            highlight_color = 'red'
        else:
            highlight_edge = (B, A)
            highlight_color = 'green'

    edge_colors = []
    for (u, v) in G.edges():
        if highlight_edge is not None and (u, v) == highlight_edge:
            edge_colors.append(highlight_color)
        else:
            edge_colors.append('gray')

    nx.draw_networkx_nodes(G, pos, node_color='lightblue', node_size=600)
    nx.draw_networkx_labels(G, pos, labels={x: str(x) for x in G.nodes()}, font_size=12)
    nx.draw_networkx_edges(G, pos, edge_color=edge_colors, arrows=True)

    # Label each edge with its weight
    edge_weights = get_edge_weights_from_digraph(G)
    e_labels = {(u, v): edge_weights[(u, v)] for (u, v) in G.edges()}
    nx.draw_networkx_edge_labels(G, pos, edge_labels=e_labels, font_color='black')

    if file_name:
        plt.savefig(file_name, bbox_inches='tight')
        plt.close()
    else:
        plt.show()

