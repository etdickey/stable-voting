def induced_subtournament(
    tournament: Dict[Tuple[int, int], int],
    subset: Set[int] | List[int],
) -> Dict[Tuple[int, int], int]:
    """
    Return the induced subtournament on 'subset' by filtering edges
    whose endpoints are both in 'subset'.
    """
    subset = set(subset)
    return {e:w for e,w in tournament.items() if e[0] in subset and e[1] in subset}

