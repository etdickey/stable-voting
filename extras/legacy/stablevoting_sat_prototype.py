# Compiled once, case-insensitive for 'x' or 'X'
_LIT_RE = re.compile(r'(~)?x(\d+)', re.IGNORECASE)
def parse_clause(clause_str: str) -> Tuple[int, Dict[int, str]]:
    """
    Parse a single clause string like "(x1, ~x2, ~x3)" into:
      - n: max variable index observed
      - polarity: dict {i: 'pos'|'neg'} for literals appearing in the clause
    e.g. "(x1, ~x2, ~x3)" -> (3, {1: 'pos', 2: 'neg', 3: 'neg'})

    Notes:
      - Duplicate literals for the same variable within a clause are allowed only
        if they have the same polarity; contradictory duplicates raise ValueError.
    """
    lits = _LIT_RE.findall(clause_str)
    if not lits:
        raise ValueError("No literals found. Expected format like '(x1, ~x2, x5)'.")
    idxs = [int(j) for _, j in lits]
    n = max(idxs)
    #the second version throws if we see two contradicting literals (x, ~x)
    # polarity = {int(j): ('neg' if s == '~' else 'pos') for s, j in lits}
    polarity: Dict[int, str] = {}
    for s, j in lits:
        i = int(j)
        pol = 'neg' if s == '~' else 'pos'
        if i in polarity and polarity[i] != pol:
            raise ValueError(f"Contradictory literals for x{i} in clause: both pos and neg.")
        polarity[i] = pol
    return n, polarity

def parse_clauses(clauses: Union[str, Iterable[str]]) -> Tuple[int, int, List[Dict[int, str]]]:
    """ Parse multiple clauses.
    Accepts:
      - A single string containing multiple parenthesized clauses, e.g.:
          "(x1, ~x2, x3) (x2, x4) ∧ (~x1, x4)"
        (any separators outside parentheses are ignored)
      - OR an iterable of clause strings, e.g.:
          ["(x1, ~x2, x3)", "(x2, x4)", "(~x1, x4)"]

    Returns:
      - n_max: maximum variable index across all clauses
      - num_clauses: number of clauses
      - clauses_polarity: list of per-clause polarity dicts {var_index: 'pos'|'neg'}
    """
    # Normalize input to a list of clause strings
    if isinstance(clauses, str):
        # Extract each (...) group; tolerate any separators between them
        groups = re.findall(r'\(([^)]*)\)', clauses)
        clause_strs = [f'({g})' for g in groups] if groups else [clauses]
    else:
        clause_strs = list(clauses)

    if not clause_strs:
        raise ValueError("No clauses provided.")

    clauses_polarity: List[Dict[int, str]] = []
    n_max = 0

    for c in clause_strs:
        n, pol = parse_clause(c)
        n_max = max(n_max, n)
        clauses_polarity.append(pol)

    num_clauses = len(clauses_polarity)
    return n_max, num_clauses, clauses_polarity

def fibonacci_series(n: int, seed1: int, seed2: int) -> List[int]:
    """
    Generates the fibonacci series of length n starting at two seed values.

    Args:
        n: The number of terms in the series.
        seed1: The first seed value.
        seed2: The second seed value.

    Returns:
        A list containing the fibonacci series.
    """
    if n <= 0:
        return []
    if n == 1:
        return [seed1]
    if n == 2:
        return [seed1, seed2]

    series = [seed1, seed2]
    while len(series) < n:
        next_term = series[-1] + series[-2]
        series.append(next_term)
    return series

