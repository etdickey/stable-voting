NUM_GROUPS = 11
def next_group():
    global curr_delta, curr_group
    if weights is None or curr_group is None or curr_delta is None:# error
        raise ValueError("error: called next_weight without initializing params")
    elif curr_group < 0:
        raise ValueError("error: called next_group too many times")
    curr_group -= 1
    curr_delta = 0
def next_weight():
    global weights, curr_group, curr_delta
    if weights is None or curr_group is None or curr_delta is None:# error
        raise ValueError("error: called next_weight without initializing params")
    curr_delta -= 1
    return weights[curr_group] + curr_delta-1
def small_jump_next_weight():
    for _ in range(10):
        next_weight()

