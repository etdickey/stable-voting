def build_graph_from_spec(clause_str:str, starting_weight:int = 1000, weight_perm: list[int] | None = None):
    global weights, curr_group, curr_delta, NUM_GROUPS
    # Parse all clauses -> n (max var index), # clauses, and per-clause polarity dicts
    n, m, clause_polarities = parse_clauses(clause_str)  # [(i -> 'pos'|'neg'), ...]
    G = nx.DiGraph()
    C = "C"
    L = [f"L{k}" for k in range(1, m+1)]
    T = [f"T{i}" for i in range(1, n+1)]
    F = [f"F{i}" for i in range(1, n+1)]
    X = [f"X{i}" for i in range(1, n+1)]
    G.add_nodes_from([C] + L + T + F + X)

    #group 1: C -> Fi, Ti
    #group 2: Fi, Ti -> Xi
    #group 3: {Ti, Fj \in clause k} -> Lk
    #group 4: Lk -> {Fi, Tj \notin clause k}
    #group 5: Xj -> {Fi, Ti \forall i!=j}
    #group 6: Xi -> Lk \forall i \forall k (... L to Xi??)
    #group 7: Lk -> C \forall k
    #group 8: Xi -> C (first relevant thing checked in negative is these edges)
    #irrelevant below (hopefully)
    #group 9: {Fi -> Ti,
    #          Fi -> Tj \forall j > i,
    #          Ti -> Fj \forall j > i} \forall i
    #group 10: Fi -> Fj \forall i < j     F->F
    #group 11: Ti -> Tj \forall i < j     T->T
    #group 12: Xi -> Xj \forall i < j     X->X
    #group 13: Li -> Lj \forall i < j     L->L

    num_groups = NUM_GROUPS

    if weight_perm is None:
        # default: Fibonacci weights
        weights = fibonacci_series(num_groups, starting_weight, starting_weight*2)
    else:
        if len(weight_perm) != num_groups:
            raise ValueError(f"weight_perm must have length {num_groups}, got {len(weight_perm)}")
        weights = weight_perm

    curr_group = num_groups-1
    curr_delta = 0


    #1) C -> Fi, Ti
    for i in range(1, n+1):
        G.add_edge(C, f"F{i}", weight=next_weight())
        G.add_edge(C, f"T{i}", weight=next_weight())
    next_group()


    #2)  Fi and Ti to xi
    for i in range(1, n+1):
        G.add_edge(f"F{i}",f"X{i}", weight=next_weight())
        G.add_edge(f"T{i}",f"X{i}", weight=next_weight())
    next_group()


    #3) {Ti, Fj \in clause k} -> Lk
    for k, pol in enumerate(clause_polarities, start=1):
        Lk = L[k - 1]
        for i, sense in pol.items():
            if sense == 'pos':
                G.add_edge(f"T{i}", Lk, weight=next_weight())
            elif sense == 'neg':
                G.add_edge(f"F{i}", Lk, weight=next_weight())
    next_group()


    #4) Lk -> {Fi, Tj \notin clause k}
    for k, pol in enumerate(clause_polarities, start=1):
        Lk = L[k - 1]
        vars_in_clause = set(pol.keys())
        for i in range(1, n + 1):
            if i in vars_in_clause:
                sense = pol[i]
                if sense == 'pos':
                    G.add_edge(Lk, f"F{i}", weight=next_weight())  # opposite of T_i
                elif sense == 'neg':
                    G.add_edge(Lk, f"T{i}", weight=next_weight())  # opposite of F_i
            else:
                # variable i not in clause k: send to both
                G.add_edge(Lk, f"F{i}", weight=next_weight())
                G.add_edge(Lk, f"T{i}", weight=next_weight())
    next_group()

    #5) Xj -> {Fi, Ti \forall i!=j}
    for i in range(1, n+1):
        Xi = f"X{i}"
        for j in range(1, n+1):
            if j != i:
                G.add_edge(Xi, f"F{j}", weight=next_weight())
                G.add_edge(Xi, f"T{j}", weight=next_weight())
    next_group()

    #6) Xi -> Lk \forall i \forall k (... L to Xi??)
    for Xi in X:
        for Lk in L:
          G.add_edge(Xi, Lk, weight=next_weight())
    next_group()

    #7) Lk -> C \forall k
    for Lk in L:
      G.add_edge(Lk, C, weight=next_weight())
    next_group()

    #8) Xi -> C
    for Xi in X:
        G.add_edge(Xi, C, weight=next_weight())
    next_group()


    # THE FOLLOWING THREE GROUPS ARE CONSIDERED THE SAME GROUP: 9, 10, 11
    #group 9: {Fi -> Ti,
    #          Fi -> Tj \forall j > i,
    #          Ti -> Fj \forall j > i} \forall i
    for i in range(1, n+1):
        G.add_edge(f"F{i}", f"T{i}", weight=next_weight())
        for j in range(i+1, n+1):
            G.add_edge(f"F{i}", f"T{j}", weight=next_weight())
        for j in range(i+1, n+1):
            G.add_edge(f"T{i}", f"F{j}", weight=next_weight())
    # next_group()
    # put a 10-weight gap between these groups we consider the "same"
    small_jump_next_weight()

    #10) Fi -> Fj \forall i < j
    for i in range(1, n+1):
        for j in range(i+1, n+1):
                G.add_edge(f"F{i}", f"F{j}", weight=next_weight())
    # next_group()
    # put a 10-weight gap between these groups we consider the "same"
    small_jump_next_weight()

    #11) Ti -> Tj \forall i < j
    for i in range(1, n+1):
        for j in range(i+1, n+1):
                G.add_edge(f"T{i}", f"T{j}", weight=next_weight())
    next_group()
    # END SAME GROUP (9-11)



    #12) Xi -> Xj \forall i < j
    for i in range(1, n+1):
        for j in range(i+1, n+1):
            G.add_edge(f"X{i}", f"X{j}", weight=next_weight())
    next_group()

    #13) Li -> Lj \forall i < j
    for i in range(1, m+1):
        for j in range(i+1, m+1):
            G.add_edge(f"L{i}", f"L{j}", weight=next_weight())
    next_group()

    return G, n, clause_polarities

"""## Tournament/Networkx graph conversion"""
def digraph_to_tournament(G: nx.DiGraph) -> Dict[Tuple[int, int], int]:
    """
    Convert a networkx.DiGraph into a tournament dictionary:
        { (u, v): weight }
    Guarantees exactly one directed edge per unordered pair {u,v}.
    """
    tournament: Dict[Tuple[int, int], int] = {}
    nodes = list(G.nodes())
    for i, u in enumerate(nodes):
        for v in nodes[i+1:]:
            if G.has_edge(u, v) and not G.has_edge(v, u):
                if "weight" not in G[u][v]:
                    raise ValueError(f"Missing weight on edge ({u}, {v})")
                tournament[(u, v)] = int(G[u][v]["weight"])

            elif G.has_edge(v, u) and not G.has_edge(u, v):
                if "weight" not in G[v][u]:
                    raise ValueError(f"Missing weight on edge ({v}, {u})")
                tournament[(v, u)] = int(G[v][u]["weight"])

            elif G.has_edge(u, v) and G.has_edge(v, u):
                raise ValueError(f"digraph_to_tournament:Both edges ({u}, {v}) and ({v}, {u}) exist in the graph.")
            else:
                raise ValueError(f"No edge between {u} and {v}, not a tournament.")
    return tournament

def tournament_to_digraph(tournament: Dict[Tuple[int, int], int]) -> nx.DiGraph:
    """ Convert a tournament dictionary back into a networkx.DiGraph with edge weights. """
    G = nx.DiGraph()
    for (u, v), w in tournament.items():
        G.add_edge(u, v, weight=w)
    return G

def tournament_to_edges(tournament: Dict[Tuple[int, int], int]) -> list[Tuple[str,str,str]]:
    """ Convert a tournament dictionary into a list of edges of the form (u,v,w). """
    edges = []
    for (u, v), w in tournament.items():
        edges.append((u,v,w))
    return edges

# def build_adj_and_edge_weights(tournament, candidates):
def build_adj_and_edge_weights(
    tournament: Dict[Tuple[int, int], int],
    candidates: Set[int],
) -> Tuple[Dict[int, List[int]], Dict[Tuple[int, int], int]]:
    adj_list = {i: [] for i in candidates}
    edge_weights = {}
    for (u, v), weight in tournament.items():
        adj_list[u].append(v)
        edge_weights[(u, v)] = weight
    return adj_list, edge_weights

