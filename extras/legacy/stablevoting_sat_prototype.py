# def margin(i, j, tournament):
def margin(i: int, j: int) -> int:
    """Return the margin of i->j."""
    if (i, j) in tourn_sv:
        return tourn_sv[(i, j)]
    else:
        return -tourn_sv[(j, i)]

# def exists_chain(start, end, tournament, threshold, candidates, visited=None):
def exists_chain(
    start: int,
    end: int,
    threshold: int,
    visited: Optional[Set[int]] = None,
) -> bool:
    """Check if there is a chain from start to end, each margin >= threshold."""
    if visited is None:
        visited = set()
    if start == end:
        return True
    visited.add(start)
    for cand in cand_sv:
        if cand not in visited:
            if margin(start, cand) >= threshold:
                if exists_chain(cand, end, threshold, visited):
                    return True
    return False

# def b_defeats_a(B, A, tournament, candidates):
def b_defeats_a(B: int, A: int) -> bool:
    """Check if B defeats A in the Stable Voting sense."""
    t = -margin(A, B)
    return not exists_chain(A, B, t, visited=set())


# def stable_voting_winner_order(candidates, tournament):
def stable_voting_winner_order() -> Tuple[Optional[int], List[int], List[str], Optional[Tuple[int, int, int]]]:
    """
    Recursively compute:
      - The Stable Voting winner,
      - The order in which other candidates are removed,
      - A textual summary (list of strings),
      - The decisive edge (A, B, margin) that "produces" the winner.
    Returns (winner, removal_order, summary, decisive_edge).
    """
    # print("Next test: candidates = ", cand_sv)
    key = frozenset(cand_sv)
    if key in sv_memo:
        return sv_memo[key]

    summary = None
    if len(cand_sv) == 1:
        single = next(iter(cand_sv))
        # summary.append(f"Base case: only candidate {single} remains.")
        result = (single, [], summary, None)
        sv_memo[key] = result
        return result

    # Build list of "valid" matches for Stable Voting
    matches = []
    for A in cand_sv:
        for B in cand_sv:
            if A == B:
                continue
            m_val = margin(A, B)
            # A->B is valid if margin >= 0 or (margin < 0 and B does not defeat A)
            if m_val >= 0 or (m_val < 0 and not b_defeats_a(B, A)):
                matches.append((A, B, m_val))
    # Sort by descending margin
    matches.sort(key=lambda x: x[2], reverse=True)

    # Recursively remove B if A->B is valid
    for (A, B, m_val) in matches:
        # new_cand = cand_sv - {B}
        cand_sv.remove(B)
        # summary.append(f"Trying removal of candidate {B} via edge ({A}, {B}) with margin {m_val} in G\\{{{B}}} = {sorted(new_cand)}")
        w, sub_order, sub_summary, sub_decisive = stable_voting_winner_order()
        cand_sv.add(B)
        # summary.extend(sub_summary)
        if w == A:
            removal_order = [B] + sub_order
            decisive_edge = (A, B, m_val)
            # summary.append(f"Edge ({A}, {B}) produced the winner: {A}.\nRemoval order so far: {removal_order}")
            result = (A, removal_order, summary, decisive_edge)
            sv_memo[key] = result
            return result

    result = (None, [], summary, None)
    sv_memo[key] = result
    return result

def stable_voting_winner_order_top(G: nx.DiGraph) -> Tuple[Optional[int], List[int], List[str], Optional[Tuple[int, int, int]]]:
    global tourn_sv, cand_sv, sv_memo
    sv_memo.clear()
    # this isn't necessary but makes me feel better
    tourn_sv.clear()
    cand_sv.clear()


    # actually set up globals
    tourn_sv = digraph_to_tournament(G)
    cand_sv = get_candidates_from_digraph(G)
    return stable_voting_winner_order()
